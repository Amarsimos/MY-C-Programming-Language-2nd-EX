/* 编写一个将输入复制到输出的程序，并将其中连续的多个空格用一个空格代替*/

#include <stdio.h>

main()
{
	int c, ns; // ns为标志位
	ns = 0;	   // 1时不在空格中，0为在空格中
	while ((c = getchar()) != EOF)// 循环读取输入字符,检测到文件结束符时退出循环
	{
		if (c == ' ')
		{
			if (ns == 1)// 如果不在空格中,则输出空格,并将ns置为0，ns为0时跳过判断，不输出
			{
				ns = 0; // 当输入space时,判断ns值,若ns=1（前字符不是space）,执行语句,输出一个space,并使ns=0无效化该判断
				putchar(c);
			}
		}

		else
		{
			ns = 1; // 输入其他键时,令ns=1,等待下一次空格输入判断
			putchar(c);
		}
	}
}
